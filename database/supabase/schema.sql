create index if not exists idx_lessons_topic on lessons(topic_id);
create index if not exists idx_lessons_order on lessons(order_num);

create table if not exists games (
  id integer generated by default as identity primary key,
  lesson_id integer not null references lessons(id) on delete cascade,
  slug varchar(100) not null,
  title varchar(255) not null,
  description text,
  path varchar(500) not null,
  game_path varchar(500) generated always as (path) stored,
  game_type varchar(50) default 'type1',
  is_active boolean default true,
  order_num integer default 0,
  order_index integer generated always as (order_num) stored,
  created_at timestamp default now(),
  updated_at timestamp default now(),
  unique (lesson_id, slug)
);

create index if not exists idx_games_lesson on games(lesson_id);
create index if not exists idx_games_order on games(order_num);
create index if not exists idx_games_path on games(path);
create index if not exists idx_games_game_path on games(game_path);

create table if not exists classes (
  id integer generated by default as identity primary key,
  code varchar(20) not null unique,
  name varchar(255) not null,
  description text,
  teacher_id integer not null references users(id) on delete cascade,
  school_year varchar(20) default '2025-2026',
  grade varchar(20),
  max_students integer default 50,
  status varchar(50) default 'active'
    check (status in ('active', 'archived', 'closed')),
